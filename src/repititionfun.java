public class repititionfun {
    public static void main(String[] args) {
        int count=0;
        while (count <= 20){
            System.out.println(count);
            count++;
        }//end while

        int count2=0;
        do{
           System.out.println(count2);
           count2++;
        }while(count2<20);

        for (int i=0; i<=100; i++){
            System.out.println(i);
        }//end for loop
    }//end main
}
